test_that("multiplication works", {
  expect_equal(snafu(1), "1")
  expect_equal(snafu(2), "2")
  expect_equal(snafu(3), "1=")
  expect_equal(snafu(4), "1-")
  expect_equal(snafu(5), "10")
  expect_equal(snafu(6), "11")
  expect_equal(snafu(7), "12")
  expect_equal(snafu(8), "2=")
  expect_equal(snafu(9), "2-")
  expect_equal(snafu(10), "20")
  expect_equal(snafu(15), "1=0")
  expect_equal(snafu(20), "1-0")
  expect_equal(snafu(2022), "1=11-2")
  expect_equal(snafu(12345), "1-0---0")
  expect_equal(snafu(314159265), "1121-1110-1=0")
})
